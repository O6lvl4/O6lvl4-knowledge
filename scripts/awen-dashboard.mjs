#!/usr/bin/env node
/**
 * awen-dashboard — render vault/dashboard.md with a per-note retention
 * table. Reads vault/.srs/<slug>/*.json directly, so it works even
 * without `awen sync` having run first.
 *
 * Sorted by:
 *   1. due-now first (next_due ≤ today)
 *   2. then ascending by next_due
 *   3. then by retention (lowest first — needs the most attention)
 */
import { readFileSync, writeFileSync, readdirSync, existsSync } from 'node:fs';
import { join } from 'node:path';

const VAULT = process.env.AWEN_VAULT || './vault';
const SRS = join(VAULT, '.srs');

function safeReaddir(p) {
  try { return readdirSync(p, { withFileTypes: true }); } catch { return []; }
}

function loadCards(slug) {
  const dir = join(SRS, slug);
  const out = [];
  for (const e of safeReaddir(dir)) {
    if (!e.isFile() || !e.name.endsWith('.json')) continue;
    try { out.push(JSON.parse(readFileSync(join(dir, e.name), 'utf8'))); } catch {}
  }
  return out;
}

function today() {
  const d = new Date();
  return `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, '0')}-${String(d.getDate()).padStart(2, '0')}`;
}

function summarize(slug) {
  const cards = loadCards(slug);
  if (cards.length === 0) return null;
  const reviews = cards.flatMap((c) => c.history || []);
  const correct = reviews.filter((r) => (r.quality ?? 0) >= 3).length;
  const retention = reviews.length === 0 ? null : correct / reviews.length;
  const lastReviewed = reviews.map((r) => r.date).filter(Boolean).sort().at(-1) ?? null;
  const nextDue = cards.map((c) => c.due).filter(Boolean).sort().at(0) ?? null;

  let state;
  if (reviews.length === 0) state = 'new';
  else if (cards.every((c) => (c.reps ?? 0) >= 2 && (c.ef ?? 0) >= 2.5)) state = 'settled';
  else if (cards.every((c) => (c.reps ?? 0) >= 1)) state = 'settling';
  else state = 'learning';

  return {
    slug,
    state,
    retention,
    cards: cards.length,
    reviewed: reviews.length,
    last_reviewed: lastReviewed,
    next_due: nextDue,
  };
}

function badge(state) {
  return ({
    new: '🟦 new',
    learning: '🟡 learning',
    settling: '🟢 settling',
    settled: '✅ settled',
  })[state] ?? state;
}

function fmt(value) { return value === null || value === undefined ? '—' : value; }

function main() {
  const slugs = safeReaddir(SRS).filter((e) => e.isDirectory() && !e.name.startsWith('.')).map((e) => e.name);
  const t = today();
  const rows = slugs
    .map(summarize)
    .filter(Boolean)
    .sort((a, b) => {
      const aDue = a.next_due && a.next_due <= t ? 0 : 1;
      const bDue = b.next_due && b.next_due <= t ? 0 : 1;
      if (aDue !== bDue) return aDue - bDue;
      const cmpDue = (a.next_due ?? '9999').localeCompare(b.next_due ?? '9999');
      if (cmpDue !== 0) return cmpDue;
      return (a.retention ?? -1) - (b.retention ?? -1);
    });

  const total = rows.length;
  const dueNow = rows.filter((r) => r.next_due && r.next_due <= t).length;
  const settled = rows.filter((r) => r.state === 'settled').length;
  const avgRet = (() => {
    const r = rows.map((x) => x.retention).filter((x) => x !== null);
    if (r.length === 0) return null;
    return r.reduce((a, b) => a + b, 0) / r.length;
  })();

  const lines = [
    '---',
    'title: Retention Dashboard',
    'tags: [meta, dashboard]',
    'public: false',
    '---',
    '',
    `<!-- auto-generated by scripts/awen-dashboard.mjs on ${t}. Do not edit by hand. -->`,
    '',
    `**Today**: ${t}  |  **Notes tracked**: ${total}  |  **Due now**: ${dueNow}  |  **Settled**: ${settled}  |  **Avg retention**: ${avgRet === null ? '—' : avgRet.toFixed(2)}`,
    '',
    '| Note | State | Retention | Cards | Reviews | Next due | Last reviewed |',
    '|---|---|---:|---:|---:|---|---|',
  ];
  for (const r of rows) {
    const ret = r.retention === null ? '—' : r.retention.toFixed(2);
    lines.push(`| [[${r.slug}]] | ${badge(r.state)} | ${ret} | ${r.cards} | ${r.reviewed} | ${fmt(r.next_due)} | ${fmt(r.last_reviewed)} |`);
  }
  lines.push('');
  lines.push('## Legend');
  lines.push('');
  lines.push('- **🟦 new** — cards captured, no reviews yet.');
  lines.push('- **🟡 learning** — at least one card not yet successfully recalled.');
  lines.push('- **🟢 settling** — every card has been correctly recalled at least once; intervals still expanding.');
  lines.push('- **✅ settled** — every card has reps ≥ 2 and ef ≥ 2.5 (long-term retention zone).');

  const outPath = join(VAULT, 'dashboard.md');
  writeFileSync(outPath, lines.join('\n') + '\n');
  console.log(`wrote ${outPath} (${total} notes)`);
}

main();
